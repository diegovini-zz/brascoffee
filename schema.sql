CREATE TABLE BEVERAGE (
	description VARCHAR(100) NOT NULL,
	id serial NOT NULL,
	cost DECIMAL NOT NULL,
	CONSTRAINT BEVERAGE_pk PRIMARY KEY (id)
) WITH (
  OIDS=FALSE
);



CREATE TABLE CONDIMENT (
	id serial NOT NULL,
	description VARCHAR(100) NOT NULL,
	cost DECIMAL NOT NULL,
	CONSTRAINT CONDIMENT_pk PRIMARY KEY (id)
) WITH (
  OIDS=FALSE
);



CREATE TABLE BEVERAGE_ORDER (
	id serial NOT NULL,
	beverage_id integer NOT NULL,
	final_price DECIMAL NOT NULL,
	CONSTRAINT BEVERAGE_ORDER_pk PRIMARY KEY (id)
) WITH (
  OIDS=FALSE
);



CREATE TABLE CONDIMENT_ORDER (
	id serial NOT NULL,
	condiment_id serial NOT NULL,
	beverageorder_id serial NOT NULL,
	CONSTRAINT CONDIMENT_ORDER_pk PRIMARY KEY (id)
) WITH (
  OIDS=FALSE
);





ALTER TABLE BEVERAGE_ORDER ADD CONSTRAINT BEVERAGE_ORDER_fk0 FOREIGN KEY (beverage_id) REFERENCES BEVERAGE(id);

ALTER TABLE CONDIMENT_ORDER ADD CONSTRAINT CONDIMENT_ORDER_fk0 FOREIGN KEY (condiment_id) REFERENCES CONDIMENT(id);
ALTER TABLE CONDIMENT_ORDER ADD CONSTRAINT CONDIMENT_ORDER_fk1 FOREIGN KEY (beverageorder_id) REFERENCES BEVERAGE_ORDER(id);
